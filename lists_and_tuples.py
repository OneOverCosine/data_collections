# What is a list?
# Lists are commonly used to store related data

shopping_list = ["bread", "chocolate", "avocados", "milk"]
print(shopping_list)

print(shopping_list[0])

shopping_list[0] = "orange"
shopping_list.append("apple juice")

shopping_list.pop(0)
print(shopping_list)

mixed_list = [1, 2, 3, "one", "two", "three"]
print(mixed_list)